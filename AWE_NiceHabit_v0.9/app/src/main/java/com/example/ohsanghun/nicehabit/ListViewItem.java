public class ListViewItem {
    private int id;
    private String habit;
    private String doneTF;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getHabit() {
        return habit;
    }

    public void setHabit(String habit) {
        this.habit = habit;
    }

    public String getDoneTF() {
        return doneTF;
    }

    public void setDoneTF(String doneTF) {
        this.doneTF = doneTF;
    }
}
